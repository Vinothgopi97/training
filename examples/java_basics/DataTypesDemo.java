// Example demonstrating Data Types in Java

public class DataTypesDemo {
    public static void main(String[] args) {
        // Primitive data types
        byte b = 100;           // 1 byte
        short s = 10000;        // 2 bytes
        int i = 100000;         // 4 bytes
        long l = 100000000L;    // 8 bytes

        float f = 10.5f;        // 4 bytes
        double d = 20.99;       // 8 bytes

        char c = 'A';           // 2 bytes
        boolean bool = true;    // 1 bit

        // Non-primitive data type (String)
        String str = "Hello Java";

        System.out.println("byte: " + b);
        System.out.println("short: " + s);
        System.out.println("int: " + i);
        System.out.println("long: " + l);
        System.out.println("float: " + f);
        System.out.println("double: " + d);
        System.out.println("char: " + c);
        System.out.println("boolean: " + bool);
        System.out.println("String: " + str);
    }
}
// Java supports primitive and non-primitive (reference) data types.
